--9. Вывести список стран, названия которых начинаются с букв от «К» до «П».
SELECT Название
 ,Столица
 ,Площадь
 ,Население
 ,Континент
FROM
Лист1
WHERE
Название LIKE N'[К-П]%'

/*10. Вывести список стран, названия которых начинаются с букв от «А» до «Г», но не 
с буквы «Б».*/
SELECT Название
 ,Столица
 ,Площадь
 ,Население
 ,Континент
FROM
Лист1
WHERE
Название LIKE N'[А-Г]%' AND Название NOT LIKE N'[Б]%'

--11. Вывести список стран, столицы которых есть в базе.
SELECT Название
 ,Столица
 ,Площадь
 ,Население
 ,Континент
FROM
Лист1
WHERE
Столица IS NOT NULL

--12. Вывести список стран Африки, Северной и Южной Америки.
